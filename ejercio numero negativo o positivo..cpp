#include<iostream>

using namespace std;

int main(){
	
	int numero;
	
	cout<<"Digite un numero : ";
	cin>>numero;
	
		
		if (numero < 0){
			cout<<"este numero es negativo";
		}
		else if (numero > 0){
			cout<<"el numero es positivo";
		}
		
		


	return 0;
}
